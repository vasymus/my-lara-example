<p>
    <b><span style="font-size: 24px;">Грунтовки</span></b>
</p>
<p>
    Делятся на 4 категории: водно-дисперсионные грунтовки, грунтовки на растворителях, полиуретановые и эпоксидные. Главное их предназначение – подготовить основание для наибольшей адгезии (сцепления) с клеем. Важно понимать, что выбор грунтовки напрямую зависит от вида выбранного клея. Полиуретановый клей предполагает полиуретановую или эпоксидную грунтовку, водный клей – водную грунтовку, а клей на растворителях лучше работает с грунтовкой на растворителях. В паркетном деле не существует универсальных грунтовок на все случаи.
</p>
