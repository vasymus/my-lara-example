<h2>3. Паркетные клеи и грунтовки</h2>
<p>
    Выбор клея так же важен, как выбор самого паркета. Поскольку именно от качественного и <b><i>правильно подобранного</i></b> клея в первую очередь зависит срок службы паркета.
</p>
<p>
    Как выбрать паркетный клей?
</p>
<p>
    Здесь учитываются десятки факторов: <a href="/articles/parketnaya-doska/porody-dereva-parket/">порода дерева</a>, размеры плашки, предполагаемая нагрузка на пол, вид основания, условия эксплуатации и т.п. При этом важно учитывать и характеристики клея: прочность и эластичность шва, время жизни (срок, в течение которого клей сохраняет прочность), рабочее время (период, в течение которого клей нужно успеть нанести), параметры шумоизоляции и пароизоляции, плотность, экологичность.
</p>
<div class="yellow-bg">
    <p>
        <b>Удельный вес (плотность) клея</b> – важный параметр. Он показывает расход на единицу площади. Бывает так, что покупатель учитывал только на цену за килограмм, а оказалось, что у дешевого клея расход получился таким большим, что более дорогой клей был бы гораздо выгодней.
    </p>
</div>
<p>
    Паркетные клеи делятся на 3 большие группы:
</p>
<ol><li>Водно-дисперсионные клеи</li>
    <li>Клеи на растворителях</li>
    <li>Полиуретановые клеи</li>
</ol><p>
    <b>1. Водно-дисперсионные клеи </b>содержат воду, что способствует короблению древесины, поэтому их используют только при работе с мелкоформатным штучным паркетом из стабильных пород (дуб, ясень, граб, берёза). Главное их преимущество – цена.
</p>
