<h2>1. Финишные покрытия</h2>
<p>
    На этапе покупки паркета вам нужно определиться с финишным покрытием. Выбор зависит от многих факторов, но прежде всего: от разновидности паркета, породы древесины и от эксплуатационных нагрузок (подробнее в <a href="/articles/parketnaya-doska/lak-ili-maslo-parket/">этой статье</a>).
</p>
<p>
    <span style="font-size: 24px;">Паркетные лаки</span>
</p>
<p>
    Лак после высыхания создаёт на поверхности паркета плёнку, которая защищает древесину от влаги, грязи и истирания. Лак оптически подчёркивает структуру дерева, делая паркет более ярким и контрастным.
</p>
<div class="yellow-bg">
    <p>
        Паркетные лаки – это НЕ лаки для дерева, они отличаются друг от друга как домашний ксерокс от профессиональной копировальной машины. Паркетные лаки, в отличие от обычных лаков для дерева, должны обладать повышенной стойкостью к истиранию. Поэтому обычные лаки, пропитки и морилки для дерева не годятся для паркетных полов. Любой производитель паркета отзовёт гарантию, если какой-то «знакомый маляр» всё-таки убедит вас и покроет паркет лаком для дерева.
    </p>
</div>
<p>
    Обновляется лаковое покрытие с помощью <a href="/tsiklevka-parketa/">циклёвки паркета</a>.
</p>
<p>
    <span style="font-size: 24px;">Как выбрать лак для паркета?</span>
</p>
<p>
    Можно выделить 3 основных вида + особняком стоят грунтовочные лаки:
</p>
<p>
    <b>1. Водные лаки</b> завоевали особую популярность, потому что экологичны, быстро сохнут и устойчивы к истиранию. Цена водных лаков высока по сравнению с другими лаковыми системами. Они подразделяются на <b>полиуретановые и акриловые</b>. Первые применяются при высоких нагрузках, вторые при умеренных.
</p>
