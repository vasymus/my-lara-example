<p>
    <span style="font-size: 24px;">Паркетные масла</span>
</p>
<p>
    Если лак создаёт на поверхности плёнку, то масло впитывается в древесину и после высыхания делает ее водонепроницаемой, стойкой к загрязнению и особо прочной. Масло усиливает положительные стороны натурального дерева и подчёркивает его текстуру. А при правильном уходе паркет под маслом, как хорошее вино, с каждым годом становится всё насыщеннее и устойчивее к внешним факторам.
</p>
<p>
    В отличие от лака, для реставрации масляного покрытия не обязательно проводить циклёвку всего пола – достаточно локальных мер.
</p>
