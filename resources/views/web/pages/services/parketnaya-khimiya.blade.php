</p>
<p>
    <b>2. Воск</b> – это пастообразная смесь из воска и натурального масла. У воска отменные характеристики по защите древесины от влаги, поэтому рекомендуется применять его в саунах, зонах у бассейнов и т.п. Часто воск используют как дополнительный финишный слой после масла-пропитки. Воск, как крем для обуви, легко восстанавливает блеск при помощи полировки.
</p>
<p>
    <b>3. </b><b>Двухкомпонентное масло</b> состоит из натурального масла с воском, которое, соединяясь c отвердителем, придает паркету очень высокие эксплуатационные характеристики, поэтому его используют в помещениях с высокими нагрузками. Для его нанесения необходимы навыки и профессиональное оборудование.
</p>
<p>
    <b>4. Масло с твёрдым воском</b> – смесь натурального масла и твердеющего воска. Действие всё то же – проникновение в структуру древесины, защита от влаги, грязи и царапин, а также на поверхности создается эффект лаковой пленки. Масла с твёрдым воском отличаются быстротой высыхания, что позволяет закончить работу за 1 смену. Быстрее высыхает только двухкомпонентное масло.
</p>
<p>
