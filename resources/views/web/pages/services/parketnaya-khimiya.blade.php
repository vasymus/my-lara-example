    Также годятся для любого вида древесины, способа укладки и типа основания. Прочность клеевого шва несколько ниже, чем у двухкомпонентного клея, поэтому есть ограничения для очень широких (от 150мм) и длинных плашек массивной доски. Но однокомпонентные клеи обладают большим временем жизни и высокой эластичностью, поэтому профессионалы берут его для <a href="/ukladka-shtuchnogo-parketa/">укладки паркета</a> без фанеры, непосредственно на бетонное основание. От двухкомпонентных клеев отличается большим рабочим временем, удобством в работе и малым расходом.
</p>
<p>
</p>
<p>
     - <u>Полимерные полиуретановые клеи</u>
</p>
<p>
    Также годятся для любого вида древесины, способа укладки и типа основания. Главное преимущество – повышенная эластичность клеевого шва, поэтому самые высокие показатели шумоизоляции. Повышенная эластичность достигается за счет уменьшения прочности клеевого шва, поэтому основное применение этот клей находит при <a href="/ukladka-parketnoy-doski/">укладке паркетной доски</a> и инженерного паркета прямо на стяжку.
</p>
