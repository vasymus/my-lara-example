<h2>Тонировка паркета – этапы.. </h2>
<ol><li>Подготовка поверхности паркета<em>.</em> Тонировать можно только идеально шлифованный, ровный и чистый пол. Поэтому первый этап – <a href="/tsiklevka-parketa/">циклёвка паркета</a>. Паркет полностью обновляется, выравнивается, очищается, шлифуется.</li>
    <li>Тонирование. Существует несколько способов тонировки паркета. Используются специальные составы на масляной основе или на растворителе. Иногда применяют «химически поджигающие» грунтовки, иногда для создания эффекта пестрой седины совмещают оба способа. Способ зависит от ваших целей – здесь нет плохих и хороших методик, есть только те, которые нужно применять в вашем случае.</li>
    <li>Нанесение финишного слоя бесцветного лака или масла на заключительном этапе.</li>
