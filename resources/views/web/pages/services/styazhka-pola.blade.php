<div class="yellow-bg">
    <p>
         Нужно различать понятия «стяжка пола» и «стяжка пола для <a href="/ukladka-shtuchnogo-parketa/">укладки паркета</a>». Паркет – элитное покрытие. Поэтому к стяжке требования жёстче.
    </p>
    <p>
         Это как с дорогами. Для обычных машин кладут один асфальт, а для спортивных трасс – совсем другой.
    </p>
    <p>
         Стяжка пола для укладки паркета должна соответствовать следующим требованиям:
    </p>
    <ul type="disc"><li>Прочность на сжатие: для обычных пород дерева ≥ 20 МПа, для экзотических пород ≥ 30 МПа (<i>для сравнения, при укладке ламината допускается прочность стяжки пола 8 МПа</i>)</li>
        <li>Влажность ≤ 4%</li>
        <li>Ровность. Допускается перепад не больше 2 мм на 2 м длины</li>
        <li>Поверхность должна быть идеально чистой и гладкой</li>
    </ul></div>
<p>
     За 10 лет практики мы в компании «Современные Технологии Паркета» сталкивались с разными ситуациями и видами стяжки для пола. Сотрудничество с клиентом, как правило, развивается по двум сценариям.
