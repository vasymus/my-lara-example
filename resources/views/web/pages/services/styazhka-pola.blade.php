<p align="center">
 <b><span style="font-size: 12pt;">Сценарий №2 – основные виды стяжек</span></b>
</p>
<p>
     В этом случае вы обращаетесь к нам для монтажа стяжки с нуля. И здесь встаёт вопрос выбора.
</p>
<p>
     Видов стяжек много – мы рассмотрим только те, которые используются для устройства паркетного пола.
</p>
<div class="white-bg">
    <p>
 <b>Цементно-песчаная стяжка (полусухая стяжка)</b> – Состоит из цемента, песка и гравия. Пропорция этих компонентов зависит от требований к прочности. Смесь разводится водой в соотношении от 15 до 25% цементной массы – критически важно не превышать водоцементное соотношение.
    </p>
</div>
<div class="white-bg">
    <p>
 <b>Полимерная стяжка</b> – Отличается от цементной тем, что в качестве связующего вещества вместо цемента используется эпоксидная, полиуретановая или акриловая смола. Полимерная стяжка обладает экстремальной прочностью и гидрофобностью (не впитывает воду), применяется только в особых случаях из-за высокой стоимости.
    </p>
</div>
<div class="white-bg">
    <p>
 <b>Самовыравнивающиеся стяжки</b> – Бывают на цементной, гипсовой или полимерной основе. Для них характерна высокая текучесть и способность к самовыравниванию. Из-за высокой цены чаще применяются как тонкий финишный слой для выравнивания обычной цементно-песчаной стяжки. Для паркетных полов оптимально подходит именно цементный «наливной пол», т.к. полимерный очень дорог, а гипсовый не годится
    </p>
</div>
