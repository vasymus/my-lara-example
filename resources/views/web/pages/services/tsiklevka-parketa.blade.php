<h3>Задача циклевки паркета многогранна:</h3>
<ol><li>удалить старое финишное покрытие (беспыльная)<br></li>
    <li>выровнять поверхность (беспыльная)</li>
    <li>подготовить паркет к нанесению нового финишного покрытия.</li>
</ol><p>
     Этим покрытием может быть лак или масло, иногда добавляется <a href="/tonirovanie-parketa/">тонировка</a> для придания паркету дизайнерских оттенков. Разница между тремя этапами шлифовки заключается в величине зерна абразива. Для удаления старого покрытия и выравнивания используют грубый абразив с крупным зерном, после него поверхность паркета становится чистой и ровной, но шершавой, не пригодной для нанесения покрытия.
</p>
<p>
     Для качественной подготовки поверхности необходима шлифовка средним и мелким абразивом, и потребуется несколько видов шлифовального оборудования. А именно: ленточная шлиф-машина, плоскошлифовальная машина и машина для обработки в труднодоступных местах возле стен. Выполнение каждого <u>из трех этапов необходимо для получения качественного паркетного пола</u>, и оно-же увеличивает стоимость работ.
</p>
