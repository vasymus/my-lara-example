<p align="center">
 <b>Циклевка – самый важный этап ремонта паркета</b>
</p>
<p>
     Скажем подробнее о циклевке, т.к. это завершающий этап восстановления паркета и обновления старого паркета до состояния «как из магазина». Процесс происходит в несколько этапов: снятие верхнего слоя, шпаклевание щелей, шлифовка, нанесение нового слоя лака или масла. Чем толще плашка паркета, тем большее количество раз его можно шлифовать. Например, циклевку штучного паркета и массивной доски можно делать до 10 раз, поэтому срок их службы 80 лет.
</p>
