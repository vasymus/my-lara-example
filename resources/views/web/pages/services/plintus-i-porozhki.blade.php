<h3 align="center">
Виды порожков </h3>
<p>
     Если по-простому, то порожек – это декоративная планка.
</p>
<p>
     В отличие от плинтусов, которые закрывают зазор  между покрытием и стеной, у порожков есть несколько функций. Поэтому разделяются они не только по виду материала, но и по функциональности:
</p>
<p>
</p>
<ul><li>
    Прямые (стыкоперекрывающие) – декорируют границу между разными напольными покрытиями, находящимися  на одном уровне. </li>
    <li>
    Разноуровневые – помогают декорировать переход между двумя напольными покрытиями, расположенными на разных уровнях. </li>
    <li>
    Завершающие (оконечные) – помогают окантовывать границу напольного покрытия. </li>
    <li>Для ступеней – декорируют ступени, препятствуют скольжению и ограждают края от быстрого разрушения. </li>
</ul><p>
</p>
<p>
 <br></p>
<p>
</p>
<p>
     Далее, как и плинтусы, порожки классифицируются в зависимости от материала:
</p>
<p>
    <br></p>
<p align="center">
 <b>Пробковые порожки (компенсаторы)</b>
</p>
<p align="center">
 <b><img width="380" alt="порожек пробковый.jpg" src="/storage/media/319/14b2732f63bf4d9c709a64f4dc35d97b.jpg" height="285" title="порожек пробковый.jpg"><br></b>
</p>
<p align="center">
</p>
<p>
     Служат для соединения приклеенного паркета с керамической плиткой и другими каменными напольными покрытиями. Податливый материал (пробка) легко уступает расширяющемуся паркетному полу, а затем возвращается на место. Не подходят для «<a href="/ukladka-parketnoy-doski/">плавающих полов</a>».
</p>
<p>
      
</p>
<p align="center">
 <b>Деревянные порожки</b>
</p>
<p align="center">
 <img width="470" alt="порожек деревянный разноуровневый.jpg" src="/storage/media/320/4bfbbf6f734b28e424754c1451272701.jpg" height="220" title="порожек деревянный разноуровневый.jpg"></p>
<p>
     Премиум сегмент паркетных аксессуаров. Деревянные порожки сделаны из массива ценной породы дерева, поэтому сочетаются с натуральным паркетом, но и <a href="/ukhod-za-parketom/">ухода</a>
    требуют такого же – их периодически обрабатывают специальными защитными составами, а при серьёзных царапинах и потёртостях можно даже сделать <a href="/tsiklevka-parketa/">шлифовку</a> с последующим покрытием лаком или маслом.
</p>
