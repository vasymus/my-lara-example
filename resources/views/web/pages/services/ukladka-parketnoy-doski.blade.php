<p>
 <b>Приклеивание паркетной доски к основанию</b>
</p>
<p>
     Это более надёжный способ укладки. По сравнению с плавающим способом он несколько дороже, потому что потребуется клей, но затраты себя оправдывают:
</p>
<ul type="disc"><li>MS-полимерный клей, на который «садится» паркетная доска, прочно её удерживает (гораздо лучше, чем только замковые соединения «плавающего способа»), поэтому срок службы дольше в 2,5 раза и составляет 15-20 лет.</li>
    <li>Эластичный клей улучшает звукоизоляцию пола – жалобы соседей снизу останутся в прошлом</li>
    <li>Проще провести ремонт паркета - легко заменить поврежденную доску, для этого не нужно разбирать пол от стены, как в случае с «плавающей укладкой»</li>
    <li>Клеевой шов доски с основанием отлично проводит тепло, и вместе с этим препятствует деформации и короблению доски, поэтому классическая технология укладки используется для монтажа полов с подогревом</li>
</ul><div class="white-bg">
    <p>
         При организации полов с подогревом разрешено использовать ТОЛЬКО водяной теплоноситель.
    </p>
    <p>
         При укладке паркетной доски обогрев должен быть выключен. После завершения работ температуру нужно повышать очень аккуратно – на 1-2 <sup>0</sup>С в сутки в течение 7-ми дней.
    </p>
</div>
<p>
     После укладки не забывайте проводить регулярный <a href="/ukhod-za-parketom/">уход за паркетом</a>
</p>
<p>
 <b>Требования к основанию под укладку паркетной доски:</b>
</p>
<ul><li>Идеальная ровность – перепад не более 2 мм на 2 м длины. При плавающей укладке толщина подложки, которая укладывается под доску, не должна превышать 3 мм. Она служит исключительно для звукоизоляции, а НЕ для выравнивания основания.</li>
    <li>Влажность стяжки не больше 4% – иначе паркет вздуется. Температура стяжки не ниже 10<sup>0</sup>С</li>
    <li>Температура воздуха в помещении 15-25<sup>0</sup>С, влажность – 45-70%</li>
</ul><p>
