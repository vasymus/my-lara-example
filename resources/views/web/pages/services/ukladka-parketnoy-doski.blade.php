<h3 style="text-align: center;">Технология укладки паркетной доски «плавающим способом»</h3>
<p>
     Этот способ востребован из-за простоты, высокой скорости и низкой цены. Доска укладывается словно ковёр – БЕЗ приклеивания к основанию. Целостность конструкции обеспечивают замковые соединения досок между собой. Посмотрите, как это выглядит на видео.
</p>
 <iframe width="100%" height="480" src="//www.youtube.com/embed/L68GcvZcQL8?rel=0" frameborder="0" allowfullscreen=""></iframe> <br><br><p>
     Почему эта технология выгодна экономически? Потому что для укладки паркета «плавающим способом»:
</p>
<ul type="disc"><li>Не нужен клей</li>
    <li>Снижены требования к прочности основания (достаточно 8 МПа )</li>
    <li>Скорость монтажа – до 40 м<sup>2</sup> за смену</li>
</ul><div class="faq-section">
    <div class="question" id="">
        <p>
             Как проверить профессионализм мастеров-паркетчиков?
        </p>
    </div>
    <div class="answer">
        <ol start="1" type="1"><li>Если вы выбрали технологию укладки «плавающим способом», то спросите у них, будут ли они проклеивать замковые соединения. Если вам ответят «да» – это повод насторожиться!</li>
        </ol><p>
             Склеивание замковых соединений - <b>ЗАПРЕЩЕНО</b>! Склеенные замки не дадут паркетному полу «дышать», в результате образуются огромные щели в центре комнаты. Замки можно обработать герметиком для защиты от проникновения влаги.
        </p>
        <ol start="2" type="1"><li>Возле стен, а также через каждые 8 м укладки нужно делать компенсационные швы, чтобы паркет имел возможность «дышать» и менять геометрию.</li>
            <li>Тяжелые, встроенные шкафы НЕЛЬЗЯ ставить на плавающий пол – это ограничивает подвижность разных частей покрытия, что может привести к скрипам и вздутиям. Участок пола под тяжелой встроенной мебелью необходимо отделить компенсационным швом.</li>
            <li>На бетонную стяжку обязательно надо стелить полиэтиленовую пленку для ограждения доски от капиллярного проникновения влаги из бетона.</li>
            <li>Нельзя укладывать «плавающий пол» на ковровые покрытия, необходимо их предварительно удалить.</li>
            <li>Перед укладкой, паркетную доску оставляют в помещении не распакованной на 48 часов.</li>
        </ol></div>
