<p style="text-align: center;">
 <strong><span style="font-size: 24px;">Стандартная технология</span></strong>
</p>
<p>
     На бетонную стяжку приклеивается фанера и лишь за тем укладывается паркет. При качественной стяжке фанера не нужна … , но в большинстве случаев основание для пола делают не профессиональные строители, которые НЕ соблюдают строительные технологии и нормы (СНиП).
</p>
<p>
     Чтобы скрыть дефекты основания, укладывают фанерный лист.
</p>
<div class="yellow-bg">
    <p>
         Использование фанеры для укладки штучного паркета НЕ решает проблему некачественной стяжки.
    </p>
    <p>
         Рано или поздно стяжка, не соответствующая СНиП по прочности, ровности и влажности, даст о себе знать (вздутие, скрипы, образование щелей) в результате понадобится целый комплекс работ по ремонту паркета.
    </p>
    <p>
         Именно поэтому, в перспективе российская технология становится дороже европейской.
    </p>
</div>
<p>
     Стандартную технологию укладки паркета наши клиенты выбирают в исключительных случаях. В основном мы идём по другому пути – делаем основание по всем требованиям СНиП, и тогда использование фанеры НЕ нужно.
</p>
