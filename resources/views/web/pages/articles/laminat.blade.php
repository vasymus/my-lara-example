        <h1>Как выбрать ламинат? Правда и мифы</h1>


    <p>Доля ламината на мировом рынке ВСЕХ напольных покрытий 10%. Это не просто внушительный, это гигантский
        показатель, особенно по сравнению с классическим паркетом.</p>

    <p>Впрочем, у Колы и Макдональдса показатели продаж тоже гигантские, но это не делает их продукты полезней! Давайте
        разбираться, в чём же секрет.</p>

    <p align="center" style="text-align: left;">Мы не занимаемся продажей ламината, поэтому вы читаете независимую
