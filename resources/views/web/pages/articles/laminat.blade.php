    <p>Начнём с основ и по порядку.</p>

    <h3>Что такое «ламинированный паркет»? Расставим акценты!</h3>

    <p>Ламинат часто называют «ламинированным паркетом». Это некорректно, такое название справедливо так же, как если
        линолеум называть паркетом на том основании, что на нем изображены паркетные планки.</p>

    <p>По факту! Ламинированный паркет – <b>полностью синтетическое покрытие</b>, которое НИКАКОГО отношения к паркету
        не имеет. Просто так сложилось исторически, что чаще всего ламинат имитирует именно паркет, да и замковая
        система соединения ламелей между собой такая же, как у паркетной доски. Поэтому и «ламинированный паркет».
        Сегодня существует ламинат имитирующий мрамор, кафельную плитку и даже ковровые покрытия.</p>

    <p></p>

    <p align="center" style="text-align: center;"><font size="5">Конструкция ламината</font></p>

    <p>Ламинированное напольное покрытие – это плашки из многослойного материала, в основе которого лежит или жесткая
        древесноволокнистая плита высокой плотности (плита HDF) или плита из поливинилхлорида высокой плотности (плита
        PVC).</p>

    <p>Оба материала чрезвычайно устойчивы к механическим нагрузкам, не подвержены короблению, не горючи в обычных
        условиях, легко поддаются обработке.</p>

    <p style="text-align: center;"><a href="/storage/media/196/afe3605f765105b8c49476e460611b17.jpg" class="gallery" title="ламинат HDF конструкция"><img src="/storage/media/196/afe3605f765105b8c49476e460611b17.jpg" alt="ламинат HDF конструкция" width="320" height="202" title="ламинат HDF конструкция" hspace="5" vspace="5"></a>


