    <h3>Способы укладки ламината</h3>

    <p>Несколько слов о замковых соединениях…! Когда-то появление у плашек обычного соединения шип-паз, позволило
        значительно повысить качество укладки паркета (середина 20-го века). Но технология замкового соединения плашек
        совершила революцию – многократно увеличилась простота и скорость укладки при чрезвычайно высоком качестве (
        практически без щелей ). Производители соревнуются, внося в конструкцию замка что-то своё.</p>

    <p>Посмотрите на некоторые из них:</p>

