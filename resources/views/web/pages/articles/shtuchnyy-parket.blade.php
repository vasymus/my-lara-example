<p align="center" style="text-align: left;">
    <span style="font-size: 24px;">3. Лак или масло?</span>
</p>
<p>
    <b>Лак</b> после высыхания образует на поверхности пола защитную плёнку, которая при регулярном уходе прослужит до 10-15 лет, затем проводится <a href="/tsiklevka-parketa/">циклевка паркета</a> и наносится новый слой лака. Лаковая пленка может создавать матовую, полуматовую или глянцевую поверхность в зависимости от вашего выбора.
</p>
