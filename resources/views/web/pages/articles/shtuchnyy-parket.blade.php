</p>
<p align="center" style="text-align: left;">
    <span style="font-size: 24px;">1. Почему именно штучный паркет?</span>
</p>
<p>
    Штучный паркет состоит из цельного массива дерева, в его производстве используют только твердую <a href="/articles/shtuchnyy-parket/porody-dereva/">древесину ценных пород</a> – дуб, ясень, бук, клен, граб, а также экзотические породы – венге, мербау, эбен, тик, орех. Каждая порода древесины имеет свою твердость, устойчивость к влажности и обладает индивидуальной цветовой гаммой. Хвойные породы не используются из-за мягкости древесины.
</p>
<div class="white-bg">
 <b>Плашка штучного паркета</b>: стандартная длина 250-600 мм, ширина 50-90 мм, толщина 15-22 мм. Длина плашки всегда кратна ширине. Например, если длина 420 мм, то ширина должна быть 60 или 70 мм. По этим параметрам вы легко можете сделать первоначальный анализ качества. У хорошего паркета геометрические размеры и пропорции строго соблюдаются.
    <p>
    </p>
</div>
<p>
    Штучный паркет сочетает в себе экологичность, красоту, долговечность и гибкие ценовые варианты. Вот основные факторы, почему владельцы квартир и домов выбирают штучный паркет:
</p>
<p>
    а) <b><i>Огромный выбор рисунков укладки</i></b> – квадраты, ёлочка, разбежка, плетёнка, ромбы, звезды и пр., а также художественный и модульный паркет. К элементам художественного паркета относят: модули, бордюры и розетки по оригинальным авторским рисункам. Штучный паркет открывает для вашей дизайнерской мысли любые горизонты. Подробнее о рисунках читайте в статье <a href="/articles/shtuchnyy-parket/vidy-ukladok/">виды укладки паркета</a>.
</p>
<p align="center">
    <i>Художественный паркет                    Рисунок – сложный квадрат                    Рисунок – двойная ёлочка</i>
</p>
