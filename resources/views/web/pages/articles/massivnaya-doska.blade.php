    <p align="center"><b>Параметр №1 – порода древесины</b></p>

    <p>Условно все породы можно разделить на «наши» – дуб, ясень, клен, и «экзотические» – палисандр, мербау, венге,
        орех и другие. Это очень обширная тема, поэтому более подробное описание мы вынесли в <a href="/articles/massivnaya-doska/porody-dereva-mass/">отдельную статью</a>.</p>

    <p>На что вам стоит обратить особое внимание в этом вопросе?</p>

    <p>1. Дуб – самая популярная порода древесины. Причины: дуб лучше других подходит к нашим климатическим условиям,
        стабилен к перепадам температуры и влажности, максимально эффективно поддается <a href="/tonirovanie-parketa/">тонированию</a>.
        Баланс цены и качества – вот самая правильная характеристика дубового паркета.</p>

    <p>2. Экзотическая древесина завозится из Южной Америки, Африки и Юго-восточной Азии. Она очень яркая и красивая, но
        в нашем климате бывает капризна. Это не страшно, капризы устраняются путём <a href="/ukhod-za-parketom/">профессионального
            ухода за паркетом</a>.</p>

