    <p align="center"><b>Параметр №4 – лак или масло?</b></p>

    <p>Вы выбрали сорт массивной доски, породу дерева и производителя. Теперь надо определиться: <a href="/articles/massivnaya-doska/lak-ili-maslo-mass/">нужен паркет под лаком или под маслом</a>?</p>

    <p>И здесь есть нюанс – для массивной доски подходит НЕ каждый паркетный лак. Всё из-за размеров и сезонного
        коробления древесины. Например, суперпрочными кислотными лаками покрывать массивный паркет нельзя, они
        недостаточно эластичны и могут растрескаться при короблении плашек. Подходят только алкидные и водные лаки.
        Идеальный вариант для массива – масло. А для крупной массивной доски это вообще единственный вариант.</p>

    <p>Особый случай – <a href="/tonirovanie-parketa/">тонирование</a>. Самый потрясающий эффект тонирование даёт именно
        на дубе, и это ещё одна причина предпочесть дубовый паркет. С помощью этой операции вы можете придать дубовой
        доске оттенок любой экзотической породы, или сделать паркет «под старину». Здесь десятки дизайнерских
        возможностей.</p>

