        <h1>Лак или масло для паркетной доски</h1>


    <div class="contenti">
        <p style="text-align: center;"><img longdesc="/покрытие паркета лаком и маслом" src="/storage/media/158/5454544524564.jpg" border="0" alt="покрытие паркета лаком и маслом" width="170" height="170">   <img longdesc="/покрытие паркета лаком и маслом" src="/storage/media/159/4445545.jpg" border="0" alt="покрытие паркета лаком и маслом" width="170" height="170">   <img longdesc="/покрытие паркета лаком и маслом" src="/storage/media/160/76876786.jpg" border="0" alt="покрытие паркета лаком и маслом" width="170" height="170"></p>

        <p style="text-align: center;"><span style="color: rgb(153, 51, 0);">при выборе покрытия для паркета главным фактором является назначение помещения</span>
        </p>

        <p> Не существует покрытия идеально подходящего для всех видов паркета и любых пород древесины. Выбор лака или
            масла зависит от условий эксплуатации паркетного пола, требований заказчика, свойств лака, типа
            паркета. </p>

        <p> а. Если помещением пользуется небольшое количество людей в домашней обуви, то следует выбрать лак для полов
            с нормальной нагрузкой. (Bona Novia, Lobadur Life, Pallmann Pall-X 94). </p>

        <p> б. Для офисных помещений, где ходят в уличной обуви, рекомендуются лаки для полов с повышенной нагрузкой.
            (Bona Traffic, Pallmann Pall-X 98, Lobadur 2K Supra) </p>

        <p> в. В помещениях с нестабильной температурой и влажностью, с большой посещаемостью (барах, ресторанах,
            музеях, магазинах) предпочтительно наносить водоотталкивающие масляно-восковые пропитки. (Bona Carls 90,
            Pallmann Magic Oil 2k, Lobasol HS 2K Impact Oil).</p>

        <p style="text-align: center;"><strong><span style="color: rgb(153, 51, 0);">свойства лаков</span></strong></p>

        <p><strong>Истираемость</strong> – стойкость к истирающим нагрузкам от обуви.
            <br><strong>Поверхностная прочность</strong> – стойкость к образованию царапин.
            <br><strong>Эластичность</strong> – стойкость к образованию трещин.
            <br><strong>Устойчивость к химикатам</strong> – устойчивость к химически агрессивным средам.
            <br><strong>Устойчивость к воде</strong> – способность защищать древесину от влаги.
            <br><strong>Устойчивость к ультрафиолету</strong> – изменение цвета под воздействием солнца.
            <br><strong>Экологическая чистота</strong> – отсутствие вредных для здоровья человека веществ.
            <br><strong>Оптические свойства</strong> – способность изменять тональность дерева (“поджигать”), и
            способность создавать матовую или глянцевую поверхность. </p>

        <p style="text-align: center;"><strong><span style="color: rgb(153, 51, 0);">лаковые и масляные системы</span></strong></p>

        <p><strong>Лаки на водной основе</strong> – популярны в последнее время. Их неоспоримыми достоинствами являются
            безвредность для здоровья, быстрота в работе, высокая стойкость к истиранию, хорошая эластичность. В
            дальнейшем, при регулярных влажных уборках, необходимо добавлять в воду специальное средство по уходу и
            защите, рекомендованное производителем. Цена водных лаков – высокая по сравнению с другими лаковыми
            системами. </p>

        <p><strong>Полиуретановые лаки на безводной основе</strong> (DD, PUR-лаки) – наиболее стойкие к воздействию воды
            и химикатов. Они отличаются высокой поверхностной прочностью и стойкостью к истиранию, быстрее других
            набирают окончательную прочность. Полиуретановые лаки на безводной основе недостаточно эластичные – хрупкие.
            Помимо этого они электризуются, что вызывает оседание пыли на лаковой поверхности. При нанесении изменяют
            тональность древесины (“поджигают”) поэтому на некоторых светлых породах дерева (клен) дают эффект
            пожелтения. Цена полиуретановых лаков на безводной основе довольно высока. </p>

        <p><strong>Лаки на основе искусственных смол</strong> (алкидные и уретан-алкидные ) – наиболее эластичные. Эти
            лаки можно использовать на полах с подогревом. Алкидные лаки обладают слабой сопротивляемостью истиранию и
            появлению царапин, меньшей устойчивостью к химикатам, им необходимо значительное время высыхания между
            слоями: обычно 12-18 часов и только в быстросохнущих версиях – 6-9 часов, что значительно дольше, чем у
            других лаковых систем. При нанесении изменяют тональность древесины. </p>

        <p><strong>Лаки на кислотных отвердителях</strong> (SH-лаки) – экономичны, очень просты и надежны в работе, не
            требовательны к температуре и влажности в помещении, очень  стойкие к истиранию и появлению царапин, долго
            сохраняют глянец.  Обладают хорошей водной- и химической стойкостью, быстро набирают прочность. Серьезным
            недостатком этих лаков является едкий запах во время нанесения. </p>

        <p><strong>Масляно-восковые пропитки</strong> – представляют собой смесь натуральных масел с добавлением сухого
            остатка. Превосходно сохраняют естественную красоту дерева, экологически чистые, не истираются, обладают
            хорошей водной- и химической стойкостью. Масляно-восковыми пропитками можно покрывать паркет и паркетную
            доску из любой породы древесины, полы с подогревом, промышленный паркет. Благодаря устойчивой к износу
            поверхности, получаемой в результате промасливания, масло используют в часто посещаемых помещениях и в
            помещениях с большим перепадом температур и влажности (барах, ресторанах, музеях, магазинах). </p>

