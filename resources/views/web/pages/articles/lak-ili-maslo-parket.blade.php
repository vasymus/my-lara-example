        <p> Масло придает полу глубокую естественность и близость к природе. Там, где натуральность – часть внутренней
            концепции интерьера, масло – лучший выбор. Оно проявляет текстуру и подчеркивает характерные оттенки дерева,
            позволяет полу “дышать”, создаёт ощущение теплой и натуральной древесины. Природное масло, которое
            используется для паркета, не содержит никаких вредных веществ. Использование цветных масел позволяет придать
            древесине оригинальный внешний вид и неожиданные оттенки. </p>
