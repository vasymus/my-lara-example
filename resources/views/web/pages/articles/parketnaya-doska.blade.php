<p>Укладка паркетной доски происходит двумя способами: </p>

<p>1) <b>классический способ</b> – приклеивание к основанию. Этим способом можно укладывать доску обоих видов, он более надежен, предпочтителен при укладке на обогреваемые полы. Для приклеивания нужно использовать эластичный полиуретановый клей на MS-полимерах, он создает шумопоглощающий клеевой шов с эффектом пароизоляции.</p>

<p>2) <b>плавающий способ</b> – доски с помощью замкового соединения крепятся между собой, но не крепятся к основанию, пригоден только для замковой доски.</p>

<p>Плавающий способ сделал укладку молниеносно быстрой. Правда, пришлось пожертвовать надежностью – со временем замковые соединения ослабевают и появляются щели. Такой способ используется в бюджетных офисных помещениях, где срок службы пола не столь важен – всё равно менять придётся через 4-5 лет. Добавьте к этому ограничения по установке тяжелой встроенной мебели, плюс шумность «плавающего пола» и достоинства этой технологии становятся не очевидными.</p>

<p>От укладки в первую очередь зависит срок службы паркетной доски. Поэтому уделите выбору бригады особое значение. Подробно мы рассматриваем укладку паркетной доски на <a href="/ukladka-parketnoy-doski/">этой странице</a>. Проходите по ссылке и пусть ваш новый пол служит не один десяток лет.</p>


