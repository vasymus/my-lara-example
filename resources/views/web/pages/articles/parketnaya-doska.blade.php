<p align="center"><b>Под лаком или под маслом?</b></p>

<p>Паркетная доска покрывается лаком или маслом на заводе и поставляется с уже готовым финишным покрытием.</p>

<p><i><u>Лак</u></i> создаёт на поверхности полимерную плёнку, которая защищает её от загрязнений и внешних воздействий. Лаковое покрытие проявляет текстуру дерева и предлагается в матовом и глянцевом вариантах.</p>

<p><i><u>Масло</u></i> проникает в поры древесины и после высыхания образует защитно-декоративное покрытие стойкое к загрязнениям и способное выдерживать высокие нагрузки на истирание, оно позволяет паркету «дышать» и делает пол более естественным. Современные масла могут создавать эффект лаковой пленки с глянцевым и полуматовым отображением.</p>

<div class="yellow-bg">
  <p>Паркетное масло в качестве финишного покрытия – это признак принадлежности доски к премиальному сегменту. Однополосная паркетная доска в большинстве случаев покрыта именно маслом. Рекомендуем выбирать натуральные расцветки древесины без тонировки, потому что красивая текстура доски премиум-класса не нуждается в маскирующем тонировании.</p>
