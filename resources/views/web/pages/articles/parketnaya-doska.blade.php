        <h1>Инженерная и паркетная доска - как они есть!</h1>









<p>Это статья для людей, которые присматриваются к паркетной доске в качестве напольного покрытия. Мы в компании «СТП» больше 10 лет работаем с паркетом и покажем вам, на что нужно обращать внимание при выборе паркетной доски.</p>

<p align="center"><b>Общие сведения</b></p>

<p>Паркетная доска – это разновидность более широкого класса напольных покрытий – инженерного паркета. Суть инженерного паркета состоит в двух- или трехслойной, поперечно склеенной конструкции плашки. Причем, ценная порода древесины присутствует только в верхнем лицевом слое толщиной от 3 до 6 мм. В производстве нижнего слоя производители используют различные материалы: это может быть фанера, хвойные ламели или дубовые рейки. Такая конструкция призвана сделать доску стабильной и устойчивой к перепадам температуры и влажности.</p>

<p>Пакетной доской принято называть инженерную доску с замковым соединением – дешевое и быстрое в укладке напольное покрытие, качество и срок службы которого соответствуют цене.</p>

<p align="center">Да, есть паркетная доска класса ПРЕМИУМ – но это исключение из правил, на ней остановимся ниже.</p>

