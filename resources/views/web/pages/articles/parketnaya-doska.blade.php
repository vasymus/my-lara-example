<p>В качестве верхнего слоя чаще всего используются дуб и ясень. Эти породы привычны к особенностям нашего климата, стойко переносят перепады температуры и влажности.</p>

<p>Но дело не только в этом. Фокус в том, что именно дуб и ясень лучше остальных пород древесины поддаются тонированию, состариванию и брашировке (поверхность приобретает контрастную структуру с благородными «морщинами»), что даёт безграничные возможности для полета дизайнерской мысли. </p>

<p>Одно но! Если перед вами ярко и густо тонированная паркетная доска, то велика вероятность, что под ярким окрасом прячется древесина с сучками и заболонью, и после первой шлифовки вы будете разочарованы.</p>

<p>Доска из экзотических пород выглядит очень эффектно, но стоит дороже дубовой. Экзотические породы из Южной Америки, Африки и Юго-Восточной Азии в нашем климате бывают «капризны», поэтому нужен регулярный и профессиональный <a href="/ukhod-za-parketom/">уход за паркетом</a>.</p>

