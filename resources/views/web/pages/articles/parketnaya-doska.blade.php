<p><i>Размеры паркетной доски:</i></p>

<p>· Длина: 1200-2500 мм.</p>

<p>· Ширина: 100-300 мм.</p>

<p>· Толщина: 12-22 мм (наиболее часто встречается доска 14 мм)</p>

<p>Толщина верхнего лицевого слоя определяет, сколько раз пол из паркетной доски можно обновить – <a href="/tsiklevka-parketa/">сделать циклевку</a>. Чем толще лицевой слой, тем большее количество раз можно обновить пол, тем теоретически дольше прослужит пол. Почему теоретически? Потому что срок службы зависит и от других факторов. Например, критическое значение имеет клей, используемый для склеивания слоёв. При производстве дешёвой паркетной доски берут водный клей с низкими эксплуатационными характеристиками и коротким сроком службы. Доска может просто расклеиться, так что до циклёвки и очередь не дойдёт.</p>

<p>Внешний, лицевой слой паркетной доски имитирует массивную доску (одно- и двухполосная) или классический штучный паркет (трёхполосная).</p>

